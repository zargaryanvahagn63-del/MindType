package vahagn.zargaryan.mindtype.tests;

/**
 * Перечисление основных типов психологических тестов в приложении.
 * Используется для управления логикой прохождения тестов и их последовательностью.
 */
public enum TestType {
    // Порядок объявления здесь соответствует порядку в очереди прогресса (currentStep)
    
    // 1. Тест на тип личности (Майерс-Бриггс)
    MBTI(0, "Тип личности"),
    
    // 2. Эмоциональный интеллект
    EQ(1, "Эмоциональный интеллект"),
    
    // 3. Большая пятерка (Часть 1: Экстраверсия, Доброжелательность)
    BIG5_PART1(2, "Большая пятерка (Часть 1)"),
    
    // 4. Стиль обучения (VARK)
    VARK(3, "Стиль обучения"),
    
    // 5. Большая пятерка (Часть 2: Сознательность, Нейротизм, Открытость)
    BIG5_PART2(4, "Большая пятерка (Часть 2)"),
    
    // 6. Темная триада (Нарциссизм, Макиавеллизм, Психопатия)
    DARK3(5, "Темная триада"),

    /**
     * Специальный тип для отображения сводных результатов Big Five.
     * Не является отдельным шагом в очереди (-1).
     */
    BIG5(-1, "Большая Пятерка (Полный)");

    public final int stepIndex; // Индекс шага в Firebase, соответствующий этому тесту
    public final String title;   // Название теста для отображения в интерфейсе

    TestType(int stepIndex, String title) {
        this.stepIndex = stepIndex;
        this.title = title;
    }

    /**
     * Находит тип теста по индексу текущего шага пользователя.
     * @param step Индекс шага из профиля пользователя.
     * @return Соответствующий TestType или null, если шаг не найден.
     */
    public static TestType fromStep(int step) {
        for (TestType type : values()) {
            if (type.stepIndex == step) return type;
        }
        return null;
    }

    /**
     * Проверяет, доступен ли данный тест пользователю.
     * @param userStep Текущий шаг прогресса пользователя.
     * @return true, если шаг пользователя >= требуемого индекса.
     */
    public boolean isAvailable(int userStep) {
        // Полный отчет Big5 доступен только после завершения всех его частей (шаг 5+)
        if (this.stepIndex == -1) return userStep >= 5;
        return userStep >= this.stepIndex;
    }

    /**
     * Проверяет, является ли этот тест именно тем, который пользователю нужно пройти сейчас.
     */
    public boolean isCurrent(int userStep) {
        return this.stepIndex == userStep;
    }
}
